    <h1>BTS will be featured in one of 23 special edition Korean national stamps in 2023</h1>
    <div style="text-align:left">
        <p>BTS will be getting their very own, special edition Korean national stamps in 2023!<br>
            On December 14, the Korea Post (the national postal service of South Korea) revealed its list of 23 special edition stamps which will be released next year to commemorate 2023. In many countries, including in South Korea, national stamps represent and honor key cultural figures, events, monuments. The selection of figures, events, or locations for each year's special edition stamps is headed by a a committee representing various fields in the arts.<br> 
            Well, in 2023, BTS will be featured in their very own, national stamp!.<br>
            Although the exact design of the new BTS stamp has yet to be confirmed, the Korea Post announced, "BTS are globally considered 21st century pop icons, who touched the lives of those around the world with their music and lyrics."
            Some other 2023 special edition stamps include Pororo, 'The Beautiful Korean Language', honey bees, and more. </p>
